/**
 * Module SQL Generator Script
 * 
 * This script generates SQL scripts for an existing module without running
 * the full registration process.
 */

import fs from 'fs';
import path from 'path';
import readline from 'readline';
import { fileURLToPath } from 'url';

// Get __dirname equivalent in ES modules
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Create readline interface for user input
const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

// Utility function to ask questions
function askQuestion(question) {
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      resolve(answer.trim());
    });
  });
}

// Utility functions for string conversion
function toCamelCase(str) {
  return str.replace(/-([a-z])/g, (g) => g[1].toUpperCase());
}

function toPascalCase(str) {
  return str.replace(/(^|-)([a-z])/g, (g) => g.slice(-1).toUpperCase());
}

async function main() {
  console.log('\n📝 Module SQL Script Generator');
  console.log('=============================\n');

  try {
    // Get module ID from command line or ask user
    let moduleId = process.argv[2];
    
    if (!moduleId) {
      // List available modules
      const projectRoot = path.dirname(__dirname);
      const modulesDir = path.join(projectRoot, 'src', 'modules');
      const modules = fs.readdirSync(modulesDir).filter(item => 
        fs.statSync(path.join(modulesDir, item)).isDirectory() &&
        !['README.md', 'moduleHelpers.ts', 'moduleMetadata.ts'].includes(item)
      );

      if (modules.length === 0) {
        console.log('❌ No modules found!');
        process.exit(1);
      }

      console.log('Available modules:');
      modules.forEach((module, index) => {
        console.log(`${index + 1}. ${module}`);
      });

      const moduleIndex = parseInt(await askQuestion('\nSelect module number: ')) - 1;
      if (moduleIndex < 0 || moduleIndex >= modules.length) {
        console.error('❌ Invalid module selection!');
        process.exit(1);
      }

      moduleId = modules[moduleIndex];
    }

    const moduleIdCamel = toCamelCase(moduleId);
    const moduleIdPascal = toPascalCase(moduleId);
    const projectRoot = path.dirname(__dirname);

    console.log(`\n📦 Generating SQL scripts for: ${moduleId}`);

    const config = {
      moduleId,
      moduleIdCamel,
      moduleIdPascal
    };

    await generateModuleSQLScripts(projectRoot, config);

    console.log('\n✅ SQL script generation completed!');
    console.log(`\n📂 Generated files in: modules/${moduleId}/scripts/`);
    console.log('   - create_tables.sql');
    console.log('   - drop_tables.sql'); 
    console.log('   - seed_data.sql');
    console.log('   - install.sql');
    console.log('   - README.md');

  } catch (error) {
    console.error('❌ Error generating SQL scripts:', error.message);
  } finally {
    rl.close();
    process.exit(0);
  }
}

